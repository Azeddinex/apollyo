export function generateWordVariations(baseWord: string): string[] {
  const variations: string[] = []

  // Add prefixes
  for (const prefix of ENGLISH_PREFIXES) {
    variations.push(prefix + baseWord)
  }

  // Add suffixes
  for (const suffix of ENGLISH_SUFFIXES) {
    variations.push(baseWord + suffix)
  }

  return variations.filter((word) => word.length >= 3 && word.length <= 15)
}

export function getRandomEnglishWords(count: number, minLength = 3, maxLength = 12): string[] {
  const words = Array.from(ENGLISH_WORD_BASE).filter((word) => word.length >= minLength && word.length <= maxLength)

  const result: string[] = []
  for (let i = 0; i < count && words.length > 0; i++) {
    const randomIndex = Math.floor(Math.random() * words.length)
    result.push(words[randomIndex])
    words.splice(randomIndex, 1)
  }

  return result
}

export function isLikelyEnglishWord(word: string): boolean {
  word = word.toLowerCase()

  // Check if it's in our common words dictionary
  if (COMMON_ENGLISH_WORDS.has(word)) {
    return true
  }

  // Check for invalid patterns
  for (const pattern of INVALID_PATTERNS) {
    if (pattern.test(word)) {
      return false
    }
  }

  // Check for English-like structure
  const hasVowel = /[aeiou]/i.test(word)
  const hasConsonant = /[bcdfghjklmnpqrstvwxyz]/i.test(word)

  if (!hasVowel || !hasConsonant) {
    return false
  }

  // Check for common prefixes/suffixes
  const hasCommonAffix =
    Array.from(ENGLISH_PREFIXES).some((prefix) => word.startsWith(prefix)) ||
    Array.from(ENGLISH_SUFFIXES).some((suffix) => word.endsWith(suffix))

  // Check for common syllables
  const hasCommonSyllable = Array.from(COMMON_SYLLABLES).some((syllable) => word.includes(syllable))

  // Word is likely English if it has common affixes or syllables
  return hasCommonAffix || hasCommonSyllable
}
